/// EVAQ Internationalization System
/// Supports: FR, EN, ZH, RU, AR
/// Based on PWA i18n.tsx dictionaries

class AppLocale {
  static const String fr = 'fr';
  static const String en = 'en';
  static const String zh = 'zh';
  static const String ru = 'ru';
  static const String ar = 'ar';

  static const List<String> supported = [fr, en, zh, ru, ar];

  static String label(String code) {
    switch (code) {
      case fr: return 'Francais';
      case en: return 'English';
      case zh: return 'Chinese';
      case ru: return 'Russian';
      case ar: return 'Arabic';
      default: return code;
    }
  }

  static String flag(String code) {
    switch (code) {
      case fr: return '🇫🇷';
      case en: return '🇬🇧';
      case zh: return '🇨🇳';
      case ru: return '🇷🇺';
      case ar: return '🇸🇦';
      default: return '🌐';
    }
  }
}


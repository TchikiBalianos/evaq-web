  static void setLocale(String l) => _locale = l;

  static String t(String key) {
    final dict = _dictionaries[_locale] ?? _dictionaries[AppLocale.fr]!;
    return dict[key] ?? _dictionaries[AppLocale.fr]?[key] ?? key;
  }

  /// Translate alert title from English to current locale
  static String translateAlertTitle(String title, String eventType) {
    if (_locale == AppLocale.en) return title;

    final typeLabel = t('event.$eventType');
    // Pattern: "Event in Country1, Country2"
    final inMatch = RegExp(r'\s+in\s+(.+)$', caseSensitive: false).firstMatch(title);
    if (inMatch != null) {
      final countries = inMatch.group(1)!
          .split(RegExp(r',\s*'))
          .map((c) => _countryNamesFr[c.trim()] ?? c.trim())
          .join(', ');
      return '$typeLabel — $countries';
    }

    // Pattern: "Tropical Cyclone NAME"
    final cycloneMatch = RegExp(r'Tropical Cyclone\s+(.+)', caseSensitive: false).firstMatch(title);
    if (cycloneMatch != null) {
      return '${t("event.TC")} ${cycloneMatch.group(1)}';
    }

    return typeLabel.isNotEmpty ? typeLabel : title;
  }

